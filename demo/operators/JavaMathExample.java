package demo.operators;

public class JavaMathExample {
	public static void main(String args[]) {
		double x=28;
		double y=4;
		//return the Maximum number of two number
		
		System.out.println("Maximum number of x and y="+" "+Math.max(x,y));
		
		//return the square root of y
		
		System.out.println("Square root of y="+" "+Math.sqrt(y));
		
		//power of x and y
		
		System.out.println("power of x and y="+" "+Math.pow(x, y));
		
		//return the log of given number
		
		System.out.println("log of x="+" "+Math.log(x));
		System.out.println("log of y="+" "+Math.log(y));
		
		//
		
	}

}
